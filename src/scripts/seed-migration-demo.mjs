const USER_ID = 'bd7d2c20-5727-4f80-a449-818429abecc9';

const admin = createClient(SUPABASE_URL, SERVICE_ROLE_KEY, {
  auth: { autoRefreshToken: false, persistSession: false },
});

const TEST_FILES_DIR = '/tmp/migration_test_files';

const FILES = [
  {
    localName: 'customers_swim_50.csv',
    entity: 'customers',
    platform: 'swim',
    rowCount: 50,
    headers: ['Customer ID','Customer Name','Email','Mobile','Ring Size','Anniversary','Birthday','Notes','Store Credit','Source'],
  },
  {
    localName: 'inventory_swim_30.csv',
    entity: 'inventory',
    platform: 'swim',
    rowCount: 30,
    headers: ['SKU','Item Name','Category','Metal','Metal Colour','Purity','Stone Type','Carat','Retail Price','Cost','QOH','Barcode','Supplier','Status'],
  },
  {
    localName: 'repairs_swim_20.csv',
    entity: 'repairs',
    platform: 'swim',
    rowCount: 20,
    headers: ['Repair ID','Customer Name','Customer Email','Item Type','Item Description','Repair Type','Stage','Quoted Price','Deposit','Due Date','Notes'],
  },
  {
    localName: 'shopify_products_25.csv',
    entity: 'inventory',
    platform: 'shopify',
    rowCount: 25,
    headers: ['Handle','Title','Body (HTML)','Vendor','Type','Tags','Published','Option1 Name','Option1 Value','Variant SKU','Variant Price','Variant Compare At Price','Variant Inventory Qty','Image Src'],
  },
];

async function main() {
  console.log('🌱 Seeding demo migration session for Marcus & Co...\n');

  // Create migration session
  const { data: session, error: sessionErr } = await admin
    .from('migration_sessions')
    .insert({
      tenant_id: TENANT_ID,
      created_by: USER_ID,
      source_platform: 'swim',
      session_name: 'Swim POS → Nexpura (Demo)',
      status: 'files_uploaded',
      mode: 'full_migration',
      data_scope: 'active_and_recent',
      notes: 'Demo migration session with real test data. Generated by seed script.',
    })
    .select()
    .single();

  if (sessionErr) {
    console.error('Failed to create session:', sessionErr.message);
    process.exit(1);
  }

  const sessionId = session.id;
  console.log(`✅ Created session: ${sessionId}\n`);

  // Upload each file
  for (const f of FILES) {
    const localPath = join(TEST_FILES_DIR, f.localName);
    if (!existsSync(localPath)) {
      console.warn(`⚠️  File not found: ${localPath} — skipping`);
      continue;
    }

    const content = readFileSync(localPath);
    const storagePath = `${TENANT_ID}/${sessionId}/${f.localName}`;

    console.log(`📤 Uploading ${f.localName}...`);
    const { error: uploadErr } = await admin.storage
      .from('migration-files')
      .upload(storagePath, content, {
        contentType: 'text/csv',
        upsert: true,
      });

    if (uploadErr) {
      console.error(`  ❌ Storage upload failed: ${uploadErr.message}`);
    } else {
      console.log(`  ✅ Uploaded to storage`);
    }

    // Get sample rows from CSV
    const text = content.toString('utf-8');
    const lines = text.split('\n').filter(l => l.trim());
    const sampleLines = lines.slice(1, 6);
    const parseLine = (line) => {
      const r = []; let cur = ''; let inQ = false;
      for (const ch of line) {
        if (ch === '"') inQ = !inQ;
        else if (ch === ',' && !inQ) { r.push(cur.trim()); cur = ''; }
        else cur += ch;
      }
      r.push(cur.trim());
      return r;
    };
    const sampleRows = sampleLines.map(parseLine);

    // Create file record
    const { data: fileRecord, error: fileErr } = await admin
      .from('migration_files')
      .insert({
        tenant_id: TENANT_ID,
        session_id: sessionId,
        original_name: f.localName,
        storage_path: storagePath,
        file_size: content.length,
        mime_type: 'text/csv',
        detected_entity: f.entity,
        detected_platform: f.platform,
        confidence_score: 0.95,
        column_headers: f.headers,
        sample_rows: sampleRows,
        row_count: f.rowCount,
        status: 'classified',
        classification_notes: `Auto-classified as ${f.entity} from ${f.platform} format`,
      })
      .select()
      .single();

    if (fileErr) {
      console.error(`  ❌ DB insert failed: ${fileErr.message}`);
    } else {
      console.log(`  ✅ DB record created: ${fileRecord.id}`);
    }
  }

  console.log(`\n🎉 Demo session ready!`);
  console.log(`   Session ID: ${sessionId}`);
  console.log(`   Tenant: Marcus & Co. Fine Jewellery`);
  console.log(`   URL: /migration/${sessionId}/files`);
}

main().catch(console.error);
