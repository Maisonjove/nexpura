interface SidebarProps {
  user: {
    full_name?: string | null;
    email?: string | null;
    role?: string | null;
    tenants?: {
      name?: string | null;
      logo_url?: string | null;
      subscriptions?: { plan: string; status: string }[] | { plan: string; status: string } | null;
    } | null;
  } | null;
  isSuperAdmin?: boolean;
}

export default function Sidebar({ user, isSuperAdmin = false }: SidebarProps) {
  const pathname = usePathname();
  const router = useRouter();
  const [mobileOpen, setMobileOpen] = useState(false);
  const supabase = createClient();

  // Determine plan
  const subs = user?.tenants?.subscriptions;
  const sub = Array.isArray(subs) ? subs[0] : subs;
  const plan = sub?.plan ?? "basic";
  const isBasicPlan = plan === "basic";

  async function handleSignOut() {
    await supabase.auth.signOut();
    router.push("/login");
    router.refresh();
  }

  const SidebarContent = () => (
    <div className="flex flex-col h-full bg-forest text-white">
      {/* Wordmark */}
      <div className="px-5 py-5 border-b border-white/10">
        <div className="flex items-center gap-2">
          {user?.tenants?.logo_url ? (
            // eslint-disable-next-line @next/next/no-img-element
            <img src={user.tenants.logo_url} alt="Logo" className="w-7 h-7 rounded object-cover" />
          ) : (
            <div className="w-7 h-7 rounded bg-sage/30 flex items-center justify-center">
              <span className="text-xs font-bold text-sage">
                {(user?.tenants?.name || "N")[0].toUpperCase()}
              </span>
            </div>
          )}
          <div>
            <span className="font-fraunces text-base font-semibold tracking-tight text-white">
              Nexpura
            </span>
            {user?.tenants?.name && (
              <p className="text-xs text-white/40 leading-none mt-0.5 truncate max-w-[140px]">
                {user.tenants.name}
              </p>
            )}
          </div>
        </div>
      </div>

      {/* Nav */}
      <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
        {NAV_ITEMS.map((item) => {
          const isExternal = (item as { external?: boolean }).external;
          const isActive = !isExternal && (pathname === item.href || pathname.startsWith(item.href + "/"));
          const isLocked = (item as { proOnly?: boolean }).proOnly && isBasicPlan;
          const className = `flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm transition-all ${
            isActive
              ? "bg-white/15 text-white font-medium"
              : "text-white/60 hover:bg-white/8 hover:text-white"
          }`;
          const content = (
            <>
              {item.icon}
              <span className="flex-1">{item.label}</span>
              {isExternal && (
                <svg className="w-3 h-3 text-white/30 flex-shrink-0" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M10 6H6a2 2 0 00-2 2v10a2 2 0 002 2h10a2 2 0 002-2v-4M14 4h6m0 0v6m0-6L10 14" />
                </svg>
              )}
              {isLocked && (
                <svg className="w-3.5 h-3.5 text-white/30 flex-shrink-0" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M12 15v2m-6 4h12a2 2 0 002-2v-6a2 2 0 00-2-2H6a2 2 0 00-2 2v6a2 2 0 002 2zm10-10V7a4 4 0 00-8 0v4h8z" />
                </svg>
              )}
            </>
          );
          return isExternal ? (
            <a key={item.href} href={item.href} target="_blank" rel="noopener noreferrer" onClick={() => setMobileOpen(false)} className={className}>
              {content}
            </a>
          ) : (
            <Link key={item.href} href={item.href} onClick={() => setMobileOpen(false)} className={className}>
              {content}
            </Link>
          );
        })}
      </nav>

      {/* User info */}
      <div className="px-3 py-4 border-t border-white/10">
        <div className="flex items-center gap-3 px-3 py-2 rounded-lg">
          <div className="w-8 h-8 rounded-full bg-sage/30 flex items-center justify-center flex-shrink-0">
            <span className="text-xs font-semibold text-sage">
              {(user?.full_name || user?.email || "U")[0].toUpperCase()}
            </span>
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-white truncate">
              {user?.full_name || "User"}
            </p>
            <p className="text-xs text-white/40 truncate capitalize">
              {user?.role || "Staff"}
            </p>
          </div>
          <button
            onClick={handleSignOut}
            title="Sign out"
            className="text-white/40 hover:text-white transition-colors flex-shrink-0"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
          </button>
        </div>
        {isSuperAdmin && (
          <Link
            href="/admin"
            onClick={() => setMobileOpen(false)}
            className="flex items-center gap-2 mt-2 px-3 py-2 rounded-lg text-xs text-sage/70 hover:text-sage hover:bg-white/8 transition-all"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M9 12l2 2 4-4m5.618-4.016A11.955 11.955 0 0112 2.944a11.955 11.955 0 01-8.618 3.04A12.02 12.02 0 003 9c0 5.591 3.824 10.29 9 11.622 5.176-1.332 9-6.03 9-11.622 0-1.042-.133-2.052-.382-3.016z" />
            </svg>
            Admin Panel
          </Link>
        )}
      </div>
    </div>
  );

  return (
    <>
      {/* Desktop sidebar */}
      <aside className="hidden lg:flex w-60 flex-shrink-0 flex-col h-full">
        <SidebarContent />
      </aside>

      {/* Mobile hamburger */}
      <button
        onClick={() => setMobileOpen(true)}
        className="lg:hidden fixed top-4 left-4 z-50 w-10 h-10 bg-forest rounded-lg flex items-center justify-center shadow-md"
      >
        <svg className="w-5 h-5 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
        </svg>
      </button>

      {/* Mobile overlay */}
      {mobileOpen && (
        <div
          className="lg:hidden fixed inset-0 z-40 bg-black/50"
          onClick={() => setMobileOpen(false)}
        />
      )}

      {/* Mobile drawer */}
      <aside
        className={`lg:hidden fixed left-0 top-0 bottom-0 w-64 z-50 transform transition-transform duration-200 ${
          mobileOpen ? "translate-x-0" : "-translate-x-full"
        }`}
      >
        <SidebarContent />
      </aside>
    </>
  );
}
